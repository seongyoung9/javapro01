package java12.st4emp;

public class emp 
{
    
    private String name ;
    private double salary;
    private static int count;
   
    
 //---------------------------------------------------------\\ 
    public emp(String name, double salary) {
        super();
        this.name = name;
        this.salary = salary;
        count += count;
    }
    //---------------------------------------------------------\\

    public static int getCount() {
        return count;
    }
    
    
    
}
