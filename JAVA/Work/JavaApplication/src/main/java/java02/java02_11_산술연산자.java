package java02;

public class java02_11_산술연산자 
{
   
    public static void main(String[] args)
    {
        
        int x = 4;
        int y = 5;
        int r = 0;
        
        r = x + y;       
        System.out.println(r);
        
        r = x - y;       
        System.out.println(r);
        
        r = x * y;      
        System.out.println(r);
        
        r = x / y;      
        System.out.println(r);
        
        r = x % y;      
        System.out.println(r);
        
                
    }
    
    
}
