package java02;

public class java02_14_비교연산자 
{
    
    public static void main(String[] args)
    {
        
        int x = 3;
        int y = 4;
        
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x > y);
        System.out.println(x < y);
        System.out.println(x >= y);
        System.out.println(x <= y);
        
    }
    
}
