package javaex;

public class oper 
{
    
    private int Add(int a, int b)
    {
        return a + b;
    }
    private int Minus(int a, int b)
    {
        return a - b;
    }
    private int Mul(int a, int b)
    {
        return a * b;
    }
    private double Div(int a , int b)
    {
        return (double)a / b;
    }
    
}
