package java02;

public class java02_01_변수선언 
{
    
    public static void main(String[] args) 
    {
        
        boolean b = true;
        char c = 'a';
        int i = 8;
        double d = 0.05;
       
        System.out.println(b);
        System.out.println(c);
        System.out.println(i);
        System.out.println(d);
        
        b = false;
        c = 'b';
        i = 100;
        d = 12.55;
        
        System.out.println(b);
        System.out.println(c);
        System.out.println(i);
        System.out.println(d);
    }
    
}